Console.WriteLine("## Tipos de Heranças ##");
// A Herança é usada para expressar um relacionamento "é um(a)" entre uma classe base e uma ou mais classes derivadas, onde as classes derivadas
// são versões especializadas da classe base

// Herança Simples     -> É o tipo de herança em que há uma classe base e uma classe derivada

// Herança Hierárquica -> Este é o tipo de herança em que existem várias classes derivadas de uma classe base.

// Herança Multinível  -> Ocorre quando uma classe é derivada de outra classe derivada.

// Herança Múltipla    -> Ocorre qunado temos uma classe base e uma classe derivada que implementa uma ou mais interfaces